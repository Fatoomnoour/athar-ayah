export function getAbsoluteAyah(surahId: number, verseNumber: number): number {
  if (surahId < 1 || surahId > 114) return 1;
  let count = 0;
  for (let i = 0; i < surahId - 1; i++) {
    count += SURAH_VERSE_COUNTS[i];
  }
  return count + verseNumber;
}

export function getSurahName(surahId: number): string {
  const surah = SURAH_LIST.find((s) => s.id === surahId);
  return surah ? surah.name : "";
}

// Spaced revision algorithm helper based on rating
export function calculateNextReview(rating: "hard" | "medium" | "easy" | "mastered", currentInterval: number = 0): {
  intervalDays: number;
  nextReviewDate: string;
} {
  let intervalDays = 1;
  switch (rating) {
    case "hard":
      intervalDays = 1;
      break;
    case "medium":
      intervalDays = 3;
      break;
    case "easy":
      intervalDays = 7;
      break;
    case "mastered":
      // If was already mastered, double the interval up to 30 days maximum
      if (currentInterval >= 14) {
        intervalDays = 30;
      } else if (currentInterval >= 7) {
        intervalDays = 14;
      } else {
        intervalDays = 7;
      }
      break;
  }

  const nextDate = new Date();
  nextDate.setDate(nextDate.getDate() + intervalDays);

  return {
    intervalDays,
    nextReviewDate: nextDate.toISOString().split("T")[0]
  };
}
